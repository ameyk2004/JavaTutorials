public class Hello {
    
    // This is a Java class named Hello.
    
    // My first Java Code to print Hello World
    
    // The main method is the entry point of the program.
    // It is a special method that Java looks for to start executing the program.
    // It is declared as public, static, and void, meaning it can be accessed by any class,
    // doesn't belong to any specific object, and doesn't return any value.
    // It takes an array of strings (args) as input parameters.
    
    public static void main(String args [])
    {
        // The System.out.println() statement prints the string "!!! Hello World !!!" to the console.
        System.out.println("!!! Hello World !!!");
    }
}
