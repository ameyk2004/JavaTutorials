public class DataTypes {
     // Java Data Types
    
    // Data types in Java specify the type of data that can be stored in a variable.
    // Java supports two categories of data types: primitive data types and reference data types.

    // 1. Primitive Data Types:

    // Example:
    byte byteVariable = 127; // 8-bit signed integer
    short shortVariable = 32000; // 16-bit signed integer
    int intVariable = 2147483647; // 32-bit signed integer
    long longVariable = 9223372036854775807l; // 64-bit signed integer
    float floatVariable = 3.14f; // 32-bit floating point number
    double doubleVariable = 3.14159; // 64-bit floating point number
    char charVariable = 'A'; // 16-bit Unicode character
    boolean booleanVariable = true; // true or false


    //Java follows UNICODE
    char c = 'k';



}
