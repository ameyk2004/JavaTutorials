public class Variables {
    // Variables allows us to store data 

    // Variables are used to store data in a program. They have a data type, a name, and a value.
    // In Java, variables must be declared with a specific data type before they can be used.

    // Variable declaration involves specifying the data type and the variable name.
    // Syntax: data_type variable_name;

    public static void main(String args[])
    {
        int num;
        num = 10;

        System.out.println(num);
    }
   


}  
