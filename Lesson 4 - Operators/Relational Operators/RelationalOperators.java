public class RelationalOperators {
    public static void main(String args[]) {
        // Relational Operators
        
        // Relational operators are used to compare values and determine the relationship between them.
        // They return a boolean value (true or false) based on whether the comparison is true or false.
        
        // Equality and Inequality Operators
        
        int a = 5;
        int b = 7;
        
        // Equality operator (==): Checks if two values are equal
        System.out.println("a == b: " + (a == b)); // Output: false
        
        // Inequality operator (!=): Checks if two values are not equal
        System.out.println("a != b: " + (a != b)); // Output: true
        
        // Greater Than and Less Than Operators
        
        // Greater than operator (>): Checks if a value is greater than another
        System.out.println("a > b: " + (a > b)); // Output: false
        
        // Less than operator (<): Checks if a value is less than another
        System.out.println("a < b: " + (a < b)); // Output: true
        
        // Greater Than or Equal To and Less Than or Equal To Operators
        
        // Greater than or equal to operator (>=): Checks if a value is greater than or equal to another
        System.out.println("a >= b: " + (a >= b)); // Output: false
        
        // Less than or equal to operator (<=): Checks if a value is less than or equal to another
        System.out.println("a <= b: " + (a <= b)); // Output: true
        
        // Summary
        
        // Relational operators are used to compare values and determine their relationship.
        // They return a boolean value (true or false) based on whether the comparison is true or false.
    }
}
