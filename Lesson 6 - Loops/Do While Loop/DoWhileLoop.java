public class DoWhileLoop {
    public static void main(String[] args) {
        // Initialization
        int i = 0;

        // Do-while loop
        do {
            // Code to be executed in each iteration
            System.out.println("i: " + i);
            
            // Increment
            i++;
        } while (i < 5); // Condition check comes after the loop body
    }
}
